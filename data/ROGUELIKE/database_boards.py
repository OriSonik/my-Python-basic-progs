BOARD_WIDTH = 40
BOARD_HEIGHT = 15

def create_board(width, height): 
    return [['.']*width for x in range(height)]

def add_exit_to_board(board, icon, x, y):
    board[x][y] = icon

def is_cell_exit(board, x, y):
    try: 
        int(board[x][y])
        return True
    except ValueError:
        return False

def create_boards():    
    board_1 = create_board(BOARD_WIDTH, BOARD_HEIGHT)
    board_2 = create_board(BOARD_WIDTH, BOARD_HEIGHT)
    board_3 = create_board(BOARD_WIDTH, BOARD_HEIGHT)
    board_4 = create_board(BOARD_WIDTH, BOARD_HEIGHT)

    board_list = {'1': board_1, '2': board_2, '3': board_3, '4': board_4}

    # ============== SET EXITS AND ENTRIES = AND PLACE TP ORB ====================
    engine.create_item_on_board(board_1, database_items.teleportation_orb, 14, 33)

    # set exit from board 1 to 2                                   
    exit_1_to_2_x = random.randrange(0, len(board_list['1']))
    exit_1_to_2_y = BOARD_WIDTH-1
    add_exit_to_board(board_list['1'], '2', exit_1_to_2_x, exit_1_to_2_y)
    # set exit from board 2 to 1                                  
    add_exit_to_board(board_list['2'], '1', exit_1_to_2_x, 0)
        
    # set exit from board 2 to 3                                   
    exit_2_to_3_x = random.randrange(0, len(board_list['2'])-2)
    exit_2_to_3_y = BOARD_WIDTH-1
    add_exit_to_board(board_list['2'], '3', exit_2_to_3_x, exit_2_to_3_y)    
    # set exit from board 3 to 2                                  
    add_exit_to_board(board_list['3'], '2', exit_2_to_3_x, 0)

    # set exit from board 2 to 4                                   
    exit_2_to_4_x = 14
    exit_2_to_4_y = BOARD_WIDTH-1
    add_exit_to_board(board_list['2'], '4', exit_2_to_4_x, exit_2_to_4_y)    
    # set exit from board 4 to 2                                  
    add_exit_to_board(board_list['4'], '2', exit_2_to_4_x, 0)
    # ============================================================================

    board_1_create(board_1)
    board_2_create(board_2)
    board_3_create(board_3)
    board_4_create(board_4)

